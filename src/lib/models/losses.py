class DisL1Loss(nn.Module):
    def __init__(self):
        super(DisL1Loss, self).__init__()
        
        self.num_interval = 3
        self.dcor_values = []
        self.dcor_plot_counter = 1
        self.epoch_distances = []

    def save_epoch_distances(self, output_dir, epoch):
        
        os.makedirs(output_dir, exist_ok=True)
        
        file_path = os.path.join(output_dir, f'epoch_{epoch}_distances.txt')

        with open(file_path, 'w') as f:
            for dis_pred, dis_gt in self.epoch_distances:
                f.write(f'{dis_pred} {dis_gt}\n')

        self.epoch_distances.clear()

    def forward(self, pred, gt, ct):
        pred = pred.squeeze()
        x = ct[0][0]
        y = ct[0][1]
        pred = pred[y, x]
        pred = pred.unsqueeze(0)
        l1_loss = F.l1_loss(pred, gt, reduction='mean')

        rmse = torch.sqrt(F.mse_loss(pred, gt, reduction='mean'))

        delta = torch.max(pred / gt, gt / pred)
        
        sigma1 = (delta < 1.25).float().mean()
        sigma2 = (delta < 1.25 ** 2).float().mean()
        sigma3 = (delta < 1.25 ** 3).float().mean()
        log10 = torch.mean(torch.abs(torch.log10(pred + 1e-6) - torch.log10(gt + 1e-6)))

        gt_flat = gt.view(-1).cpu().numpy() 
        pred_flat = pred.view(-1).cpu().detach().numpy()  

        interval_loss = torch.zeros(self.num_interval)
        if 0 <= gt < 50:
            interval_loss[0] = l1_loss
        elif 50 <= gt < 100:
            interval_loss[1] = l1_loss
        elif 100 <= gt < 200:
            interval_loss[2] = l1_loss

        self.epoch_distances.append((pred.item(), gt.item()))

        return l1_loss, interval_loss, rmse, sigma1, sigma2, sigma3, log10
        
